<h1 class="section">3.3 Potential Risks and Side Effects</h1>
<p>While gentle protocols like Mini-IVF&trade; offer significantly lower risks and fewer side effects when compared to conventional IVF, any prescribed medications may have side effects and can pose a risk to your health.  To best manage these risks, it helps to be informed.  Please carefully review the potential risks and medication side effects below and discuss any questions or concerns with your personal care team.</p>

<h3 class="no-lines">Potential Risks</h3>

<p><strong>Ovarian Hyper Stimulation Syndrome (OHSS)</strong></p>
<p>Ovarian Hyper Stimulation Syndrome is the most common risk associated with conventional IVF, although very uncommon, it can also occur in minimal stimulation cycles.  During ovarian stimulation, the maturation of a large number of follicles within the ovary can cause the ovary to swell.  As the follicles grow, the eggs inside are surrounded by fluid.  When many eggs are retrieved, fluid can accumulate in the abdomen or the chest, requiring hospitalization.  Vomiting, diarrhea and shortness of breath are symptoms of severe OHSS, but discomfort and ovarian tenderness are also indicators.  If you experience any of these symptoms, contact your doctor immediately.</p>

<p><strong>Multiple Births</strong></p>
<p>Multiple embryo transfers can result in a high-order pregnancy and the health risks to the mother and child are significant.  Additionally, the chances of premature delivery increase with higher-order multiples.  We advocate single embryo transfer to eliminate the risk of multiple births.</p>

