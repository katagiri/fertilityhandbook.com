<h1 class="section">3.4 Frequently Asked Questions</h1>
<p>Below is a list of frequently asked questions.  If the following answers do not fully address your questions or concerns, please do not hesitate to contact a member of your personal care team.</p>

<p><strong>Why do some fertility treatments begin with birth control pills?</strong><br />
Birth control pills do not affect a woman's ability to become pregnant once she has discontinued their use.  Rather, the medication regulates a woman's cycle to synchronize follicle development prior to ovarian stimulation.</p>

<p><strong>Is there an age limit for Mini IVF&trade;?</strong><br />
We do not have an age limit for pre-menopausal women.</p>

<p><strong>If I decide to use a surrogate, are there special considerations?</strong><br />
There are complex legal issues associated with surrogacy that should be first discussed with an attorney who specializes in surrogacy prior to making decisions on treatment.</p>

<p><strong>What percentage of patients choose Mini-IVF&trade;?</strong><br />
Ninety-five percent of our patients choose this protocol.</p>

<p><strong>Who is a candidate for Mini-IVF&trade;?</strong><br />
Mini-IVF&trade; is suitable for all pre-menopausal women even if they have responded poorly to previous conventional IVF attempts.</p> 

<p><strong>Can stress or changes to my normal routine affect the treatment process?</strong><br />
Yes.  It is necessary to reduce stress and changes to your daily routine during the entire treatment process.</p>

<p><strong>Why does Mini-IVF&trade; require fewer hormones?</strong><br />
Exposure to high doses of hormones can mature a large quantity of eggs at the expense of egg quality.  Lower hormone dosages help the body produce only the best quality eggs a woman can mature in one cycle to produce a healthy baby. </p>

<p><strong>How long will I take medications before egg retrieval?</strong><br />
Most women take medication for nine to 13 days.</p>

<p><strong>Will all my retrieved eggs be transferred?</strong><br />
The number of eggs retrieved is not necessarily the number of embryos viable for transfer.</p>

<p><strong>What is Comparative Genomic Hybridization (CGH)?</strong><br />
CGH is used to detect genetic abnormalities in eggs and embryos.  Specifically, CGH is used to analyze the copy number changes in DNA to identify abnormal regions in the genome.</p>

<p><strong>Does New Hope perform multiple embryo transfers?</strong><br />
At New Hope, we promote single-embryo transfers to reduce the risks associated with multiple births.  Surplus embryos can be frozen using our vitrification method and stored in your personal embryo bank for later use.</p>

<p><strong>Clomid has been used since the 1980s. Are newer and more efficacious drugs available in place of Clomid?</strong><br />
Clomid remains the most prescribed fertility drug worldwide and is considered the first line in infertility treatment medication.</p>

<p><strong>Are Clomid success rates better today compared to when it was first introduced?</strong><br />
Clomid success rates have improved markedly due to technical advances in the field of assisted reproduction.</p>

<p><strong>How often can I take a course of Clomid?</strong><br />
We generally allow patients to use Clomid for five to ten cycles.</p>

<p><strong>Can women over 35 use Clomid or is more aggressive hormone therapy required?</strong><br />
Women over 35 do very well with natural and low stimulation cycles using Clomid.  These women generally produce higher FSH in response to Clomid because they have a smaller ovarian reserve than younger women and therefore do not usually require the FSH injections used in conventional therapy.</p>  

<p><strong>Why does Mini-IVF&trade; use Synarel instead of HCG?</strong><br />
HCG has a long half-life, causing the stimulation of immature follicles which can lead to cyst formation in subsequent treatment cycles.  Synarel, alternatively, is strong enough to induce ovulation of larger follicles, but has a very short half-life.  This preserves smaller follicles for future cycles, rather than stimulating them prematurely.  As a result, the chances of healthy egg production are increased and women can cycle continuously.  Synarel is especially advantageous for older patients with a limited ovarian reserve.</p>

<p><strong>How do you know I will not ovulate prior to retrieval?</strong><br />
During your menstrual cycle, an estrogen sensor activates the hypothalamus to trigger an LH surge, which induces final maturation when the lead follicle reaches its optimal size.

Clomid causes the body to think estrogen levels are low, which results in the release of FSH and LH.  These hormones ultimately stimulate follicular production and the release of mature eggs.  Over time, though, Clomid acts to block the production of LH, which helps reduce the chances of premature ovulation.</p>

<p><strong>Why is injectable FSH recommended for some women undergoing Mini-IVF&trade;?</strong><br />
Often, patients with a very low baseline FSH cannot produce enough of the hormone with Clomid and require additional FSH to aid follicular development.</p>

<p><strong>Do fertility drugs cause cysts?</strong><br />
Sometimes an immature follicle can evolve into a cyst in the following cycle.  In particular, HCG injections can facilitate cyst formation.  The presence of a cyst does not necessarily mean you cannot begin another cycle.  We minimize the risk of cyst formation by using Synarel as a trigger instead of HCG.</p>

<p><strong>What happens if I do not become pregnant?</strong><br />
If your treatment is not successful, your personal care team will meet with you to discuss your options.</p>

