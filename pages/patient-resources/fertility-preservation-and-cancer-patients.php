<h1 class="section">4.1 Fertility Preservation and Cancer Patients</h1>
<p>Your Personal Care Team at New Hope will work with you and your oncology team to coordinate care and design a tailored treatment plan.  Many of the fertility treatments outlined in this handbook are available to patients who are survivors of various types of cancer or are looking to preserve their fertility prior to undergoing cancer treatment.</p>
<p>New Hope offers gentle stimulation techniques to aid in fertility preservation through oocyte or embryo cryopreservation.  From the start of stimulation to banking, the process usually lasts three to four weeks and facilitates a speedy return to all other necessary treatments.  A combination of Femara or Tamoxifen with gonadotropin injections has proven safe and effective in such protocols (drug information below).</p>
<p>We require the approval of your treating oncologist and ask that you provide all medical records pertaining to your cancer treatment including surgical and biopsy reports, as well as pathology reports and any tumor marker tests.</p>

