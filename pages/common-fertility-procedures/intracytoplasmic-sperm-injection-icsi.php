<h1 class="section">2.6 Intracytoplasmic Sperm Injection (ICSI)</h1>
<p>Intracytoplasmic sperm injection is a micromanipulation technique used in cases of male factor infertility where normal sperm quantity and motility is impaired.  With ICSI, a single sperm is selected on the basis of its shape and size and then injected into the cytoplasm of a mature egg to achieve fertilization (see Figure 2.2).</p>


