<h1 class="section">1.5 The Male Reproductive System</h1>

<p>While the male reproductive system is also influenced by hormonal changes, it differs from the female reproductive system in many ways.  Unlike women, men produce their sex cells, called sperm, throughout sexual maturity.  Additionally, male reproductive organs are both internal and external.  Using the diagram (Figure 1.6) as a guide, familiarize yourself with the main parts of the male reproductive system.</p>

<ul class="book">
	<li>The <em>penis</em> is one of the external parts of the male reproductive system.  Other external parts are the testes, which make sperm and secrete the hormone testosterone.</li>
    <li><em>Sperm</em> are stored and mature in the epididymis, the tissue layer surrounding the testes.  The production and maturation of sperm takes approximately 72 hours and sperm remain viable within the epididymis for several months.</li>
    <li>From the <em>epididymis</em>, sperm travel via the <a title ="The vasa deferentia are the ducts that carry sperm from the epididymis during ejaculation.">vas deferens</a> to a walnut-sized gland called the prostate.  The prostate surrounds the urethra and produces prostatic fluid that is added to semen.</li>
    <li>Located above the prostate gland are <em>seminal vesicles</em>, which produce seminal fluid and join the vas deferens at the ejaculatory duct.</li>
</ul>


